class Product {
    constructor(
      id,
      title,
      description,
      color,
      size,
      imageUrl,
      price
    ) {
      this.id = id;
      this.title = title;
      this.description = description;
      this.color = color;
      this.size = size;
      this.imageUrl = imageUrl;
      this.price = price;
    }
  }
  
  export default Product;
  